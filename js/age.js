let age = 0
if (age <= 1) {
    console.log("infant")
}
else if (age >= 2 && age <= 4) {
    console.log('toddler')
}
else if (age >= 5 && age <= 8) {
    console.log('child')
}
else if (age > 9 && age <= 13) {
    console.log('preteen')
}
else if (age > 14 && age <= 19) {
    console.log('teen')
}
else if (age = 20) {
    console.log('young adult')
}